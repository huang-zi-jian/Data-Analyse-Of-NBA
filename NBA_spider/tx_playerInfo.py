url = "https://ziliaoku.sports.qq.com/cube/index?cubeId=10&dimId=52&from=sportsdatabase&order=t70&params=t2:2019|t3:1|&limit=0,200"
url_player = "https://ziliaoku.sports.qq.com/cube/index?&cubeId=8&dimId=5&params=t1:{}&from=sportsdatabase"
headers = {'User-Agent': random.choice(user_agent)}
# 随机获取一个请求头
def get_user_agent():
    return random.choice(user_agent)

def get_team_dict():
    resp = requests.get(url=url, headers=headers)
    text = resp.text
    jsonobj = json.loads(text)
    data_json = jsonobj["data"]["nbaPlayerSeasonStatRank"]
    df = pd.DataFrame(data_json)
    position = []
    team_name = list(set(list(df['teamName'])))
    dic_Team = dict([(k, []) for k in team_name])
    for row in df.itertuples():
        url_player_format = url_player.format(getattr(row, 'playerId'))
        page_text = requests.get(url = url_player_format, headers = headers).text
        json_obj = json.loads(page_text)
        print(json_obj["data"]["playerBaseInfo"]["position"])
        position.append(json_obj["data"]["playerBaseInfo"]["position"])
        mylist = [getattr(row, 'cnName'), json_obj["data"]["playerBaseInfo"]["position"]]
        dic_Team[getattr(row, 'teamName')].append(mylist)
    print(dic_Team)
    file = shelve.open('./files/dict_team_player/team_player.dat')
    file['dic_team_player'] = dic_Team
    file.close()

    df['position'] = pd.DataFrame(position)

if __name__ == '__main__':
    get_team_dict()
